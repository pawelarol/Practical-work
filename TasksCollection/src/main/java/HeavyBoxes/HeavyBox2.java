package HeavyBoxes;

public class HeavyBox2 {
    protected int weight;
    protected int id;


    public HeavyBox2(int weight, int id) {
        this.weight = weight;
        this.id = id;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
